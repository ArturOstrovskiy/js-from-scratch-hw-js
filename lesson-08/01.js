function capitalizeWords(someWords) {
	//split'–æ–º –º—ã —Å–æ–∑–¥–∞–ª–∏ –º–∞—Å—Å–∏–≤ someWords –∏ —Ä–∞–∑–¥–µ–ª–∏–ª–∏ –µ–≥–æ –ø–æ –ø—Ä–æ–±–µ–ª–∞–º (' ')
	//map'–æ–º –º—ã –∫ –∫–∞–∂–¥–æ–º—É —ç–ª–µ–º–µ–Ω—Ç—É –º–∞—Å—Å–∏–≤–∞ –ø—Ä–∏–º–µ–Ω–∏–ª–∏ —Ñ—É–Ω–∫—Ü–∏—é
	return someWords.split(' ').map(function (word) {

			//—Ñ—É–Ω–∫—Ü–∏—è –±–µ—Ä–µ—Ç –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç –º–∞—Å—Å–∏–≤–∞ –∏ –Ω–∞—Ö–æ–¥–∏—Ç —É –Ω–µ–≥–æ —ç–ª–µ–º–µ–Ω—Ç –ø–æ–¥ –∏–Ω–¥–µ–∫—Å–æ–º 0
			//–ø–æ—Å–ª–µ —á–µ–≥–æ –ø—Ä–∏–º–µ–Ω—è–µ—Ç –∫ –Ω–µ–º—É –º–µ—Ç–æ–¥ toUpperCase()
			//–ø–æ—Å–ª–µ —á–µ–≥–æ .slice(1) –¥–æ–±–∞–≤–ª—è–µ—Ç –∫ —ç–ª–µ–º–µ–Ω—Ç—É —Å –∏–Ω–¥–µ–∫—Å–æ–º 0 –æ—Å—Ç–∞–≤—à–µ–µ—Å—è —ç–ª–µ–º–µ–Ω—Ç—ã –Ω–∞—á–∏–Ω–∞—è —Å –∏–Ω–¥–µ–∫—Å–∞ 1 –∏ –¥–æ –ø—Ä–æ–±–µ–ª–∞
			return word[0].toUpperCase() + word.slice(1);
			//.join(' ') —Ñ–æ—Ä–º–∏—Ä—É–µ—Ç –∏–∑ –º–∞—Å—Å–∏–≤–∞ —Å—Ç—Ä–æ–∫—É —Å –ø—Ä–æ–±–µ–ª–∞–º–∏ –º–µ–∂–¥—É —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏

	}).join(' ');
}
