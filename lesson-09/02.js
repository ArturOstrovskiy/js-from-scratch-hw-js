	//—á—Ç–æ–±—ã –Ω–µ–±—ã–ª–æ –Ω–µ—Å–∫–æ–ª—å–∫–æ –æ–¥–Ω–æ–≤—Ä–µ–º–µ–Ω–Ω–æ —Ä–∞–±–æ—Ç–∞—é—â–∏—Ö —Ç–∞–π–º–µ—Ä–æ–≤.
